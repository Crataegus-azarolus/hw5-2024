import json
import pandas as pd
import pathlib
from typing import Union
import numpy as np

class QuestionnaireAnalysis:
    """
    Reads and analyzes data generated by the questionnaire experiment.
    Should be able to accept strings and pathlib.Path objects.
    """

    def __init__(self, data_fname: Union[pathlib.Path, str]):
        self.data_fname = pathlib.Path(data_fname)
        
        filename = pathlib.Path(data_fname)
        if not filename.exists():
            raise ValueError("File not found. Cannot proceed.")
                   
    def read_data(self):
        """Reads the json data located in self.data_fname into memory, to
        the attribute self.data.
        """
        with open(self.data_fname) as f:
            table = json.load(f)
        self.data = pd.DataFrame.from_dict(table)

    def show_age_distrib(self) -> tuple[np.ndarray, np.ndarray]:
        """Calculates and plots the age distribution of the participants.
    
        Returns
        -------
        hist : np.ndarray
          Number of people in a given bin
        bins : np.ndarray
          Bin edges
        """
        ages = list(range(0, 101, 10))
        categories = []
        for i in range((len(ages)-1)):
            categories.append((ages[i], ages[i+1]))
        
        categories = np.array(categories)

        clean_ages = np.array([self.data['age'][i] for i in range(len(self.data['age'])) if isinstance(self.data['age'][i], int)])
        hist = np.array([np.sum((clean_ages >= categories[i][0]) & (clean_ages < categories[i][1])) for i in range(len(categories))])

        bins = np.array((range(0, 101, 10)))
        
        return hist, bins
    
    def remove_rows_without_mail(self) -> pd.DataFrame:
        """Checks self.data for rows with invalid emails, and removes them.

        Returns
        -------
        df : pd.DataFrame
        A corrected DataFrame, i.e. the same table but with the erroneous rows removed and
        the (ordinal) index after a reset.
        """
        for i in range(len(self.data)):
            if self.data['email'][i].count('@') != 1:
                self.data.drop(i, inplace=True)
            elif (self.data['email'][i].startswith('@')) or (self.data['email'][i].endswith('@')):
                self.data.drop(i, inplace=True)
            elif self.data['email'][i].count('.') == 0:
                self.data.drop(i, inplace=True)
            elif (self.data['email'][i].startswith('.')) or (self.data['email'][i].endswith('.')):
                self.data.drop(i, inplace=True)
            elif self.data['email'][i][self.data['email'][i].find('@') + 1] == '.':
                self.data.drop(i, inplace=True)
                
        self.data.reset_index(drop=True, inplace=True)

        return self.data

    def fill_na_with_mean(self) -> tuple[pd.DataFrame, np.ndarray]:
        """Finds, in the original DataFrame, the subjects that didn't answer
        all questions, and replaces that missing value with the mean of the
        other grades for that student.
    
        Returns
        -------
        df : pd.DataFrame
          The corrected DataFrame after insertion of the mean grade
        arr : np.ndarray
              Row indices of the students that their new grades were generated
        """     
        q_cols = ['q1','q2','q3','q4','q5']
        self.data[q_cols] = self.data[q_cols].replace(to_replace=['nan'], value=np.nan)
        
        q_df = self.data[q_cols]
        rows_with_NA = q_df.isna()
        indices = rows_with_NA.any(axis=1).index[rows_with_NA.any(axis=1) == True]
        indices = np.array(indices)
        
        
        subjects_mean = self.data[q_cols].mean(axis=1)
        for i in q_cols:
            self.data[i].fillna(pd.Series(subjects_mean), inplace=True)


        
        return (self.data, indices)
    
    def score_subjects(self, maximal_nans_per_sub: int = 1) -> pd.DataFrame:
        """Calculates the average score of a subject and adds a new "score" column
        with it.
    
        If the subject has more than "maximal_nans_per_sub" NaN in his grades, the
        score should be NA. Otherwise, the score is simply the mean of the other grades.
        The datatype of score is UInt8, and the floating point raw numbers should be
        rounded down.
    
        Parameters
        ----------
        maximal_nans_per_sub : int, optional
            Number of allowed NaNs per subject before giving a NA score.
    
        Returns
        -------
        pd.DataFrame
            A new DF with a new column - "score".
        """
        q_cols = ['q1','q2','q3','q4','q5']
        self.data[q_cols] = self.data[q_cols].replace(to_replace=['nan'], value=np.nan)

        score_column = pd.Series([np.nan] * len(self.data))
        for i in range(len(self.data)):
            if self.data[q_cols].loc[i].isna().sum() <= maximal_nans_per_sub:
                score_column.loc[i] = self.data[q_cols].loc[i].mean()
        
        self.data['score'] = score_column

        self.data['score'] = np.floor(self.data['score'])
        self.data['score'] = self.data['score'].astype(pd.UInt8Dtype())
        
        return self.data